<script setup lang="ts">
import { ILLUSTRIOUS_18, FAB_4 } from '~/utils/engine/counting'
import {
  TWENTY_ONE_PLUS_THREE_PAYS, LUCKY_LADIES_PAYS, MATCH_THE_DEALER_PAYS, BUSTER_PAYS
} from '~/utils/engine/sideBets'
import { PRESETS } from '~/utils/engine/rules'
import { BUCKETS, PROB, actionEVs, distFor, houseEdge } from '~/utils/engine/basicStrategy'
import type { EngineFacts } from '~/utils/learnContent'
import {
  HILO_ROWS, PROCEDURE, VARIATIONS, glossary, historyEras, myths, tidbits, viability
} from '~/utils/learnContent'

const store = useBlackjackStore()
onMounted(() => {
  if (!store.sessionActive) store.restore()
})

const activeRules = computed(() => store.settings?.rules ?? PRESETS.VEGAS_STRIP_6D!)

// ── every number below comes from the engine, never transcribed (guidelines §2.2/§4) ─────
const payoutDeltaPct = computed(() => ((houseEdge({ ...activeRules.value, blackjackPayout: '6:5' })
  - houseEdge({ ...activeRules.value, blackjackPayout: '3:2' })) * 100).toFixed(1))
const h17DeltaPct = computed(() => ((houseEdge({ ...activeRules.value, dealerHitsSoft17: true })
  - houseEdge({ ...activeRules.value, dealerHitsSoft17: false })) * 100).toFixed(1))
const dasDeltaPct = computed(() => ((houseEdge({ ...activeRules.value, doubleAfterSplit: false })
  - houseEdge({ ...activeRules.value, doubleAfterSplit: true })) * 100).toFixed(2))
const dealerBustPct = computed(() => Math.round(
  BUCKETS.reduce((s, up) => s + PROB[up] * distFor(up, activeRules.value).bust, 0) * 100))
const sixteenVsTen = computed(() => {
  const evs = actionEVs({ total: 16, soft: false, twoCards: true, fromSplit: false }, 10, activeRules.value)
  return { standLoss: Math.round(-evs.stand * 100), hitLoss: Math.round(-evs.hit * 100) }
})

const tabs = [
  { label: 'Strategy chart', value: 'chart' },
  { label: 'Rules lab', value: 'rules' },
  { label: 'Counting', value: 'counting' },
  { label: 'Side bets', value: 'sidebets' },
  { label: 'History', value: 'history' },
  { label: 'Myths', value: 'myths' },
  { label: 'Procedure', value: 'procedure' },
  { label: 'Glossary', value: 'glossary' }
]
const tab = ref('chart')

/** Engine-derived figures the content interpolates — computed above, never transcribed. */
const facts = computed<EngineFacts>(() => ({
  payoutDeltaPct: payoutDeltaPct.value,
  h17DeltaPct: h17DeltaPct.value,
  dasDeltaPct: dasDeltaPct.value,
  dealerBustPct: dealerBustPct.value,
  sixteenVsTen: sixteenVsTen.value
}))

// the prose lives in ~/utils/learnContent — this page is structure, not copy
const HISTORY_ERAS = computed(() => historyEras(facts.value))
const VIABILITY = computed(() => viability(facts.value))
const TIDBITS = computed(() => tidbits(facts.value))
const MYTHS = computed(() => myths(facts.value))
const GLOSSARY = computed(() => glossary(facts.value))

const DEVIATIONS = [...ILLUSTRIOUS_18, ...FAB_4]

const SIDEBET_TABLES = [
  { name: '21+3 (MA §28(f) / AC Xtreme)', pays: TWENTY_ONE_PLUS_THREE_PAYS },
  { name: 'Lucky Ladies (MA §24)', pays: LUCKY_LADIES_PAYS },
  { name: 'Buster Blackjack (MA §27)', pays: BUSTER_PAYS }
]
const MTD = MATCH_THE_DEALER_PAYS
</script>

<template>
