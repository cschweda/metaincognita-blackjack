      </p>
      <StrategyChartView :rules="activeRules" />
    </section>

    <section v-else-if="tab === 'rules'">
      <RuleExplorer />
    </section>

    <section
      v-else-if="tab === 'counting'"
      class="space-y-3 text-sm text-neutral-300"
    >
