        Tidbits
      </h2>
      <ul class="list-disc space-y-1.5 pl-5 text-xs text-neutral-300">
        <li
          v-for="tidbit in TIDBITS"
          :key="tidbit"
        >
          {{ tidbit }}
        </li>
      </ul>
    </section>

    <section
      v-else-if="tab === 'myths'"
      class="grid gap-3 sm:grid-cols-2"
